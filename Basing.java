public class Basing {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        int c = 4;
        int result1 = a-b-c;
        System.out.println(result1);
        long d = 5;
        long e = 6;
        long f = 7;
        double result2 = (d*e)/f;
        System.out.println(result2);
    }
}
